package com.example.libmana.model;

public class ThanhVien {
    public int maTV;
    public String hoTen;
    public String namSinh;
    public String ghiChu;
    public ThanhVien() {
    }

    public ThanhVien(int maTV, String hoTen, String namSinh, String ghiChu) {
        this.maTV = maTV;
        this.hoTen = hoTen;
        this.namSinh = namSinh;
        this.ghiChu = ghiChu;
    }
}
